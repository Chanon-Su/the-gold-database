"""
Valitine — Mode 2: Business Checks
Check semantic correctness of specific columns.
Each check updates a living report file for the table.
"""

import re
import pandas as pd
from datetime import datetime
from pathlib import Path


# ─────────────────────────────────────────────
# Internal: Report file management
# ─────────────────────────────────────────────

def _get_report_path(table: str, output_dir: str) -> Path:
    Path(output_dir).mkdir(parents=True, exist_ok=True)
    return Path(output_dir) / f"{table}_business_check.md"


def _load_or_init_report(df: pd.DataFrame, table: str, output_dir: str) -> dict:
    path = _get_report_path(table, output_dir)
    if path.exists():
        state = _parse_existing_report(path, df)
    else:
        state = {col: {"check": None, "status": "⬜ pending", "issues": "", "updated": ""} for col in df.columns}
    return state


def _parse_existing_report(path: Path, df: pd.DataFrame) -> dict:
    state = {col: {"check": None, "status": "⬜ pending", "issues": "", "updated": ""} for col in df.columns}
    with open(path, encoding="utf-8") as f:
        lines = f.readlines()
    for line in lines:
        if not line.startswith("|") or "Column" in line or "---" in line:
            continue
        parts = [p.strip() for p in line.strip().strip("|").split("|")]
        if len(parts) < 6:
            continue
        col_name = parts[1].strip("`")
        if col_name in state:
            state[col_name] = {
                "check": parts[2] if parts[2] != "—" else None,
                "status": parts[3],
                "issues": parts[4] if parts[4] != "—" else "",
                "updated": parts[5] if len(parts) > 5 else "",
            }
    return state


def _save_report(state: dict, table: str, output_dir: str, df_cols: list):
    path = _get_report_path(table, output_dir)
    run_date = datetime.now().strftime("%Y-%m-%d %H:%M")

    checked = sum(1 for v in state.values() if v["check"] is not None and v["status"] != "— skip")
    skipped = sum(1 for v in state.values() if v["status"] == "— skip")
    total = len(state)

    lines = []
    lines.append("# Valitine — Mode 2: Business Check")
    lines.append("")
    lines.append(f"**Table:** `{table}`  ")
    lines.append(f"**Last updated:** {run_date}  ")
    lines.append(f"**Progress:** {checked}/{total} columns checked  &nbsp;|&nbsp;  {skipped} skipped")
    lines.append("")
    lines.append("## Column Checklist")
    lines.append("")
    lines.append("| # | Column | Check Applied | Status | Issues | Updated |")
    lines.append("|---|--------|---------------|--------|--------|---------|")

    for i, col in enumerate(df_cols, 1):
        s = state.get(col, {"check": None, "status": "⬜ pending", "issues": "", "updated": ""})
        check_applied = s["check"] if s["check"] else "—"
        issues = s["issues"].split("\n")[0][:80] if s["issues"] else "—"  # first line only in table
        updated = s["updated"] if s["updated"] else "—"
        lines.append(f"| {i} | `{col}` | {check_applied} | {s['status']} | {issues} | {updated} |")

    lines.append("")

    # Issue detail section
    issue_cols = [(col, s) for col, s in state.items() if s.get("issues") and s["status"] != "— skip"]
    if issue_cols:
        lines.append("## Issue Details")
        lines.append("")
        for col, s in issue_cols:
            lines.append(f"### `{col}`")
            lines.append(s["issues"])
            lines.append("")

    lines.append("---")
    lines.append("*Generated by Valitine — Mode 2 (Business Check)*  ")
    lines.append("*⬜ pending = ยังไม่ได้เช็ค &nbsp;|&nbsp; — skip = ไม่จำเป็นต้องเช็ค (กรอกเองได้)*")

    with open(path, "w", encoding="utf-8") as f:
        f.write("\n".join(lines))

    print(f"[Valitine] Mode 2 report updated → {path}")


def _update_col(df, col, table, output_dir, check_name, issues, passed):
    state = _load_or_init_report(df, table, output_dir)
    now = datetime.now().strftime("%Y-%m-%d %H:%M")
    status = "✅ pass" if passed else "⚠️ warn"
    issue_text = "\n".join(f"- {i}" for i in issues) if issues else ""
    state[col] = {"check": check_name, "status": status, "issues": issue_text, "updated": now}
    _save_report(state, table, output_dir, list(df.columns))
    return status, issues


def _print_result(check, col, status, issues):
    print(f"[Valitine] {check}(`{col}`) → {status}")
    for issue in issues:
        print(f"           ↳ {issue}")


